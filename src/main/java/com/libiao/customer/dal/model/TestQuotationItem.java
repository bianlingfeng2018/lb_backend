package com.libiao.customer.dal.model;

public class TestQuotationItem {
    private Long id;

    private Long testQuotationId;

    private String reportNum;

    private String productDes;

    private String style;

    private String materialColor;

    private String testItem;

    private String unitPrice;

    private String qty;

    private String amountRmb;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getTestQuotationId() {
        return testQuotationId;
    }

    public void setTestQuotationId(Long testQuotationId) {
        this.testQuotationId = testQuotationId;
    }

    public String getReportNum() {
        return reportNum;
    }

    public void setReportNum(String reportNum) {
        this.reportNum = reportNum;
    }

    public String getProductDes() {
        return productDes;
    }

    public void setProductDes(String productDes) {
        this.productDes = productDes;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getMaterialColor() {
        return materialColor;
    }

    public void setMaterialColor(String materialColor) {
        this.materialColor = materialColor;
    }

    public String getTestItem() {
        return testItem;
    }

    public void setTestItem(String testItem) {
        this.testItem = testItem;
    }

    public String getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(String unitPrice) {
        this.unitPrice = unitPrice;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getAmountRmb() {
        return amountRmb;
    }

    public void setAmountRmb(String amountRmb) {
        this.amountRmb = amountRmb;
    }
}